public class Cooling
{
	Long start;
	Long end;
	int startIndex;
	int endIndex;
	Double allHeat;




	public Cooling(Long start, Long end, int startIndex, int endIndex, Double allHeat) {
		super();
		this.start = start;
		this.end = end;
		this.startIndex = startIndex;
		this.endIndex = endIndex;
		this.allHeat = allHeat;
	}
	







}
